const c = 5;
const d = 10;
const e = '100';
let f = 6;

// Add
console.log(c + d); // 15

// Add and multiply
console.log((c + d) * e); // 1500

// Remainder
console.log(d % c); // 0

// Increment
f++; // f += 1, f = f + 1 => f = 7
console.log(f); // 7

// Decrement
f--; // f -= 1, f = f - 1 => f = 6
console.log(f); // 6

// Unary negation
console.log(-c); // -5

// Unary plus
console.log(+c); // 5

// Exponentiation
console.log(c ** 2); // 25

// Division
console.log(d / c); // 2.5
